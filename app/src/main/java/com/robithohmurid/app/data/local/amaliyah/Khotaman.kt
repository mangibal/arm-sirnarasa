   ~Do'a Khotaman~

   **بِسْمِ اللّٰهِ الرَّحْمٰنِ الرَّحِيْمِ
   عَسَى اللّٰهُ أَن يَجْعَلَ بَيْنَكُمْ وبَيْنَ الَّذِيْنَ عَادَيْتُم مِّنْهُم مَّوَدَّةً وَاللّٰهُ قَدِيْرٌ وَاللّٰهُ غَفُوْرٌ رَّحِيْمٌ، رَبِّ إِنِّي ظَلَمْتُ نَفْسِي فَاغْفِرْ لِي ذَنْبِيْ، لَا إِلٰهَ إِلَّا أَنْتَ سُبْحَانَكَ إِنِّي كُنتُ مِنَ الظَّالِمِينَ (x3)**

   _Bismillaahhir_ _rohmaanir  rohhiim_

   _'Asalloohu  ayyaj'ala  bainakum  wa  bainal  ladziina  'adaitum  minhhum  mawadataw  walloohhu  qodiiruw  walloohhu  ghofuurur  rohiimu  robbi  innii  zholamtu  nafsii  faghfirlii  dzambii  laa  ilaahha  illa  angta  subhaanaka  innii  kungtu  minazh  zhoolimiina  (3x)._

   Semoga Alloh  menumbuhkan  kasih  sayang  di  antara  kalian dan  termasuk  pada orang-orang yang  memusuhi  kalian.  Alloh  Maha  Kuasa dan  Maha  Pengampun  lagi  Maha  Penyayang.  Tiada  Tuhan  selain  Engkau.  Maha  Suci  Engkau,  sesungguhnya  aku  adalah  orang yang  menganiaya  kepada  diriku  sendiri.


   **اَللّٰهُمَّ صَحًّا صَحًّا صَحًّا وَحًّا بَحًّا حٰمۤ لَا يُنْصَرُوْنَ ، وَجَعَلْنَا مِنْ بَيْنِ أَيْدِيهِمْ سَدًّا وَّمِنْ خَلْفِهِمْ سَدًّا فَأَغْشَيْنَاهُمْ فَهُمْ لَا يُبْصِرُوْنَ ، كۤهٰيٰعۤصۤ حٰمۤ عۤسۤقۤ لَا يُصَدَّعُوْنَ عَنْهَا وَلَا يُنْزِفُوْنَ ، يَا رَبُّ يَا رَبُّ يَا رَبُّ وَلَاحَوْلَ وَلَاقُوَّةَ إِلَّا بِاللّٰهِ الْعَلِيِّ الْعَظِيْم (x3)**

   _Alloohhumma_ _shohhan-shohhan-shohhan  wa  han  bahan  haa-mim  laa  yungshoruuna  wa  ja'alnaa  mim  baini  aydiihhim  saddaw  wa  min  kholfihhim  saddang  fa-aghsyainaahhum  fahhum  laa  yubshiruuna  kaf-hha-ya-'ain-shod-ha-mim-'ain-sin-qof  laa  yushodda'uuna  'anhhaa  wa  laa  yungzifuuna  yaa  robbu-yaa  robbu-yaa  robbu  wa  laa  haula  wa  laa  quwwata  illa  billaahhil  'aliyyil  'azhiimi  (3x)._

   Ya Alloh,  sehatkanlah-sehatkanlah-sehatkanlah,  tuluskanlah  ha-mim  orang yang  zholim  tidak  akan  mendapat  pertolongan. Dan Kami  adakan  di  hadapan  mereka  dinding, dan di  belakang  mereka  dinding. Dan Kami  tutup  mata  mereka,  sehingga  mereka  tidak  melihat,  kaf-hha-ya-'ain-shod-ha-mim-'ain-sin-qof,  tiadalah  mereka  mematahkan  daripada-Nya, dan  tidaklah  mereka  bisa  mengeluarkan.  Ya  Rob-Ya  Rob-Ya  Rob,  tiada  daya  dan  upaya  kecuali  dari  Alloh  Yang  Maha  Luhur  dan  Maha  Agung.



   **بِسْمِ اللّٰهِ الشَّافِي بِسْمِ اللّٰهِ الْكَافِي بِسْمِ اللّٰهِ الْمَعَافِي بِسْمِ اللّٰهِ الَّذِي لَا يَضُرُّ مَعَ اسْمِهِ شَيْءٌ فِي الْأَرْضِ وَلَا فِي السَّمَاءِ وَهُوَ السَّمِيعُ الْعَلِيمُ (x3)**

   _Bismillaahisy_ _syaafii  bismillaahhil  kaafii  bismillaahhil  ma'aafii  bismillaahhil  ladzii  laa  yadlurru  ma'asmihhii  syai-um  fiil  ardli  wa  laa  fiis  samaa-i  wa  hhuwas  samii'ul  'aliimu  (3x)._

   Dengan Nama  Alloh  Yang  Maha  Menyembuhkan,  dengan  Nama  Alloh  Yang  Maha  Mencukupi,  dengan  Nama  Alloh  Yang  Maha  Menyehatkan,  dengan  Nama  Alloh  yang  melalui  Nama-Nya  segala  sesuau  yang  ada  di  bumi  dan di  langit  tidak  membahayakan. Dan  Dia-lah  Yang  Maha  Mengetahui.


   **رَبَّنَا أَنْزِلْنَا مُنْزَلًا مُّبَارَكًا وَّأَنْتَ خَيْرُ الْمُنْزِلِيْنَ (x3)**

   _Robbanaa_ _angzilnaa  mungzalam  mubaarokaw  wa  angta  khoirul  mungziliina  (3x)._

   Ya Tuhanku,  tempatkanlah  aku  pada  tempat  yang  penuh  berkah. Dan  Engkaulah  Dzat  yang  sebaik-baiknya  memberikan  tempat.


   **رَبَّنَا افْتَحْ بَيْنَنَا وَبَيْنَ قَوْمِنَا بِالْحَقِّ وَأَنْتَ خَيْرُ الْفَاتِحيْنَ (x3)**

   _Robbanaftah_ _bainana  wabaina  qouminaa  bil  haqqi  wa  anta  khoirul  faatihiin  (3x)._

   Ya Tuhanku,  bukakanlah  di  antara  kami dan di  antara  kaum  kami  kebenaran. Dan  hanya  Engkaulah  sebaik-baik  pembukanya.

   **رَبَّنَاۤ أَفْرِغْ عَلَيْنَا صَبْرًا وَثَبِّتْ أَقْدَامَنَا وَانْصُرْنَا عَلَى القَوْمِ الْكَافِرِينَ (x3)**


   _Rabbana_ _afrigh  'alaina  shabran  wa  tsabbit  aqdamana  wanshurna  'alal  qoumil  kafirin  (3x)._

   Ya Tuhanku,  limpahkanlah  kesabaran  atas  diri  kami,  kokohkanlah  pendirian  kami  serta  tolonglah  kami  dalam  mengalahkan  orang-orang kafir.
   **بِسْمِ اللّٰهِ الرَّحْمٰنِ الرَّحِيْمِ**
   **اَلْحَمْدُ لِلّٰهِ رَبِّ الْعٰلَمِيْنَۙ الرَّحْمٰنِ الرَّحِيْمِۙ مٰلِكِ يَوْمِ الدِّيْنِۗ اِيَّاكَ نَعْبُدُ وَاِيَّاكَ نَسْتَعِيْنُۗ اِهْدِنَا الصِّرَاطَ الْمُسْتَقِيْمَۙ صِرَاطَ الَّذِيْنَ اَنْعَمْتَ عَلَيْهِمْ ەۙ غَيْرِ الْمَغْضُوْبِ عَلَيْهِمْ وَلَا الضَّاۤلِّيْنَ**

