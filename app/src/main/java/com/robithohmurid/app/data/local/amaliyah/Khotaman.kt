   **يَا لَطِيْفٌ (x3)
   ياَ مَنْ وَسِعَ لُطْفُهُ أَهْلَ السَّمٰوَاتِ وَاْلأَرْضِ نَسْأَلُكَ بِخَفِيِّ ِخَفِيِّ لُطْفِكَ الـْخَفِيِّ أَنْ تُخْفِيْنَا فِي خَفِيِّ خَفِيِّ لُطْفِكَ الـْخَفِيِّ إِنَّكَ قُلْتَ وَقَوْلُكَ الْحَقُّ: اَللّٰهُ لَطِيْفٌ بِعِبَادِهٖ يَرْزُقُ مَنْ يَشَآءُ وَهُوَ الْقَوِيُّ الْعَزِيْزَ .
   اَللّٰهُمَّ إِنَّا نَسْألُكَ يَاقَوِيُّ يَاعَزِيْزُ يَامُعِيْنُ بِقُوَّتِكَ وَعِزَّتِكَ يَامَتِيْنُ أَنْ تَكُوْنَ لَنَا عَوْناً وَمُعِيْنًا فِي جَمِيْعِ الْأَقْوَالِ وَالْأَحْوَالِ وَالْأَفْعَالِ وَجَمِيْعِ مَا نَحْنُ فِيْهِ مِنْ فِعْلِ الـْخَيْرَاتِ، وَأَنْ تَدْفَعَ عَنَّا كُلَّ شَرٍّ وَنِقْمَةٍ وَمِحْنَةٍ قَدِاسْتَحْقَيْنَاهَا مِنْ غَفْلَتِنَا وَذُنُوْبِنَا، فَإِنَّكَ أَنْتَ الْغَفُوْرُ الرَّحِيْمُ وَقَدْ قُلْتَ وَقَوْلُكَ الْحَقُّ : وَيَعْفُوا عَنْ كَثِيْرٍ .
   اَللّٰهُمَّ بِحَقِّ مَنْ لَطَفْتَ بِهٖ وَوَجَّهْتَهٗ عِنْدَكَ وَجَعَلْتَ اللُّطْفَ الـْخَفِيِّ تاَبِعاً لَهٗ حَيْثُ تَوَجَّهَ، نَسْأَلُكَ أَنْ تُوَجِّهَنَا عِنْدَكَ وَأَنْ تُخْفِيَنَا بِلُطْفِكَ إِنَّكَ عَلٰى كُلِّ شَيْئٍ قَدِيْرٌ .
   وَصَلَّى اللّٰهَ عَلٰى سَيِّدِنَا مُحَمَّدٍ وَعَلٰى اٰلِهٖ وَصَحْبِهٖ وَسَلَّمَ وَالْحَمْدُ لِلّٰهِ رَبِّ الْعَالَمِيْنَ .**

   _Yaa  lathiif_ (3x)

   _Yaa Man  wasi’a  lathfuhuu  ahlas  samaawaati  wal  ardh,  nas’aluka  bi  khofiyyi  khofiyyi  luthfi-Kal  khofiyyi,  antukhfiyanaa  fii  khofiyyi  khofiyyi  Luthfi-Kal  khofiyyi,  Innaka  qulta  wa  qowlu-Kal  haqqu :  "Alloohu  lathiifum  bi ’ibaadihi  yarzuku  may  yasyaa’u  wa  Huwal  Qowiyyul  'Aziizu"._

   _Alloohumma_ _innaa  nas’aluka  yaa  Qowiyuu  yaa  'Aziizu  ya  Mu’iinu  bi  quwwati-Ka  wa  ‘izzati-Ka Yaa  Matiinu  an  takuuna  lanaa  ’awnaw  wa  mu’iinan  fii  jamii’il  aqwaali  wal  ahwaali  wal  af’aali  wa  jamii’i  maa  nahnu  fiihi  min  fi’lil  khoyrooti,  wa  antadfa’a  anna  kulla  syarriw  wa  niqmatiw  wa  mihnatin  qodistahqoynaahaa  min  ghoflatina  wa  dzunuubinaa, fa  inna-Ka  Antal  Ghofuurur  Rohiim,  wa  qod  qulta  wa  qowlukal  haqqu: "Wa  ya’fuu  ‘an  katsiir",_

   _Alloohumma_ _bi  haqqi  man  lathofta  bihi  wa  wajjahtahu  ‘indaka  wa  ja’altal  luthfal  khofiyyi  taabi’al  lahuu  haytsu  tawajjaha,  nas’aluka  an  tuwajjihanaa  ‘indaka  wa  antukhfianaa  bi  luthfika  innaka  ‘alaa  kulli  syai’in  Qodir.  Wa  shollalloohu  ‘alaa  Sayyidinaa  Muhammadiw  wa  ‘alaa  aalihi  wa  shohbihi  wa  sallam. Wal  hamdulillaahi  Robbil’aalamiin._

   Dengan nama Alloh  yang  Maha  Pengasih  lagi  Maha  Penyayang.  Wahai  Zat  yang  Lembut  (3x).  Wahai  Zat  yang Luas  Kelembutannya  kepada  penghuni  langit  dan  bumi  kami  semua  mohon  pada-Mu  dengan  segala  rahasia  Kelembutan-Mu agar  dirahasiakan  kami  semua  didalam  rahasia  Kelembutan-Mu.  Sesungguhnya  Engkau  telah  berfirman  dan  firman-Mu  itu  adalah  benar,  Alloh  itu  Maha  Lembut  terhadap  hamba-hamba-Nya  memberi  rezki  kepada  orang yang  dikehendakinya.  Dia  adalah  Zat  Yang  Maha  Perkasa  Lagi  Maha  Mulia. Yaa  Alloh,  sesungguhnya  kami  semua  mohon  pada-Mu  wahai  Zat  Yang  Maha  Perkasa,  Maha  Mulia,  Maha  Penolong.  Dengan  Keperkasaan-Mu dan  Kemuliaan-Mu  Wahai  Zat  Yang  Maha  Kuatsemoga  Engkau  menolong  kami,  dalam  segala  perkataan,  perilaku,  pekerjaan  dan  segala  sesuatu  kebaikan  yang kami  kerjakan, dan  semoga  Engkau  menolak  dari  kami,  dari  setiap  kejelekan,  kecelakaan, dan  malapetaka.  Sedangkan  karena  kelalaian  kami dan  dosa-dosa  kami  itu  semua pantas  bagi  kami.  Sesungguhnya  Engkau  Zat  Yang  Maha  Pengampun  dan  Penyayang. Dan  sesungguhnya  Engkau  telah  berkata,  sedangkan  kata-kata-Mu  itu  adalah  benar  “dan  Engkau  akan  mengampuni  dari  semuanya”. Yaa  Alloh  dengan  sesungguhnya  orang yang  telah  Engkau  kasihani  dan  telah  Engkau  hadapkan  disisi-Mu dan  telah  Engkau  jadikan  rahasia  kelembutan-Mu  itu  padanya  tatkala  dia  menghadap-Mu, kami  semua  mohon  pada-Mu agar  dihadapkan  kami  disisi-Mu dan  dirahasiakan  kami  dengan  kelembutan-Mu,  sesungguhnya  Engkau  atas  segala  sesuatu  Yang  Maha  Kuasa. Dan  semoga  Alloh  memberi  rahmat  dan  keselamatan  pada Nabi Muhammad,  keluarga  dan para  sahabatnya,  Segala  puji  bagi  Alloh,  Tuhan Semesta  Alam.

